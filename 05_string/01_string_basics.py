# String Concatenation
name = "Manish"
print("Hello, " + name)

# Using escape characters to include double quotes
a = "He said, \"I want to eat an apple\""
print(a)

# Using single quotes to avoid escaping double quotes
b = 'He said, "I want to eat an apple"'
print(b)

# Using triple quotes for multiline strings
x = '''He said,
how are you?
I replied: nice.
Then he said goodbye.'''
print(x)
