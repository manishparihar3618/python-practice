# Basic Arithmetic Operations in Python

print(5 + 4)        # Addition: 9
print(21 - 13)      # Subtraction: 8
print(9 * 1)        # Multiplication: 9
print(9 / 2)        # Division (float result): 4.5

print(14 // 8)      # Floor Division (removes decimal): 1
print(15 % 6)       # Modulus (remainder): 3
print(5 ** 3)       # Exponentiation (5 to the power of 3): 125
